////////////////////////////////////////////////////////////////////////////////////////////////////////////
void Phase4::changePhase(WarPhaseChanger* WPC) 
{
    if(WPC->getStrategy()=="Negotiate" )
    {
        if(WPC->Status()==true)
        {
            WPC->setPhase(new Phase5());
        }
        else
        {
            WPC->setPhase(new Phase3());
        }
 
    }
   
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////

string Phase4::getPhase()
{
    return "Post-Conflict";
}

