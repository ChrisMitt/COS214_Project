void Phase5::changePhase(WarPhaseChanger* WPC) 
{
    if(WPC->getStrategy()=="Diplomacy" && WPC!=NULL)
    {
        if(WPC->Status()==true)
        {
            cout<<"Diplomacy succeded and War is Over"<<endl;
        }
        else
        {
            WPC->setPhase(new Phase2());
        }
 
    }
    
}

